import { mapGetters } from "vuex";
import MessageHandlerMixin from "../mixins/MessageHandlerMixin";

export default {
  name: "MainMenu",
  mixins: [MessageHandlerMixin],
  computed: {
    ...mapGetters(["mainMenuOpen"]),
  },
  components: {
  },
  data() {
    return {
      werkDetails: false,
      showOG: false,
      label: "",
      werkTitle: "",
      werkImg: "",
      werkAuthor: "",
      werkYear:"",
      werkLocation: "",
      werkStart: "",
    };
  },
  methods: {
    setGuideInfo(title, img, author, year, loc, start) {
      this.werkTitle = title;
      this.werkImg = img;
      this.werkAuthor = author;
      this.werkYear = year;
      this.werkLocation = loc;
      this.werkStart = start;
      this.werkDetails = true;
      this.selectWerk();
    },
    selectWerk() {
      $('circle').on("click", function() {
        $('circle.selected').attr("class", "");
        $(this).attr("class", "selected");
      });
    },
    resetSelection() {
      let guides = document.querySelectorAll(".werk");
      for (let i = 0; i < guides.length; i++) {
        guides[i].classList.remove("selected");
      }
    },
    switchFloor(floor) {
      if(floor === 'eg') {
        this.showOG = false;
      } else if(floor === 'og') {
        this.showOG = true;
      }

      this.resetSelection();
    },
    startGuide() {
      if (this.werkTitle && this.werkStart != "") {
        this.mainMenuOpen = "menu-hidden";
        this.sendTriggerToBot({
          text: `Starte ${this.werkTitle}!`,
          payload: this.werkStart,
        });
      }
    },
    // pulls Info from svg to display it in hover tooltip
    getInfo(e) {
      this.label = e.target.id;
    },
  },
};
</script>

